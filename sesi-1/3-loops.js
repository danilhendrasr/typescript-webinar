// Regular for loop
for (let i = 0; i < 10; i++) {
    console.log(i);
}

// While loop
let i = 0;
while (i < 5) {
    console.log(i);
    ++i;
}

// For of
const numbersForOf = [1, 2, 3];
for (const num of numbersForOf) {
    console.log(num);
}

// For in
const numbersForIn = [4, 5, 6];
for (const num in numbersForIn) {
    console.log(num);
}
